package liasons;
import java.util.*;

public class Appartenir {
    private int idO;
    private int idC;


    /**
     * @param idO
     * @param idI
     */
    public Appartenir(int idO, int idI) {
        this.idO = idO;
        this.idC = idC;
    }
    /**
     * @return int
     */
    public String getidO() {
        return idO;
    }

    /**
     * @return int
     */
    public String getidI() {
        return idI;
    }


    /**
     * @param idC
     */
    public void setIdC(int idC) {
        this.idC = idC;
    }

    /**
     * @param idO
     */
    public void setIdO(int idO) {
        this.idO = idO;
    }


}
